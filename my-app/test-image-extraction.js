// Function ƒë·ªÉ l·∫•y h√¨nh ·∫£nh t·ª´ element v·ªõi nhi·ªÅu selector
function extractImageFromElement($element, source) {
    let image = null;

    // Th·ª≠ c√°c selector h√¨nh ·∫£nh theo th·ª© t·ª± ∆∞u ti√™n
    for (const imgSelector of source.imageSelectors) {
        const imgElement = $element.find(imgSelector);
        if (imgElement.length > 0) {
            image = imgElement.attr('src') ||
                imgElement.attr('data-src') ||
                imgElement.attr('data-lazy-src') ||
                imgElement.attr('data-original') ||
                imgElement.attr('data-url') ||
                imgElement.attr('data-thumb');

            if (image && image.length > 10 &&
                !image.includes('placeholder') &&
                !image.includes('loading') &&
                !image.includes('avatar') &&
                !image.includes('icon') &&
                !image.includes('logo') &&
                !image.startsWith('data:image/svg')) {
                console.log(`üñºÔ∏è  T√¨m th·∫•y h√¨nh ·∫£nh t·ª´ ${imgSelector}: ${image}`);
                break;
            }
        }
    }

    // N·∫øu kh√¥ng t√¨m th·∫•y, th·ª≠ v·ªõi selector ch√≠nh
    if (!image) {
        const mainImgElement = $element.find(source.selector.image);
        if (mainImgElement.length > 0) {
            image = mainImgElement.attr('src') ||
                mainImgElement.attr('data-src') ||
                mainImgElement.attr('data-lazy-src') ||
                mainImgElement.attr('data-original') ||
                mainImgElement.attr('data-url') ||
                mainImgElement.attr('data-thumb');

            if (image && image.length > 10 &&
                !image.includes('placeholder') &&
                !image.includes('loading') &&
                !image.includes('avatar') &&
                !image.includes('icon') &&
                !image.includes('logo') &&
                !image.startsWith('data:image/svg')) {
                console.log(`üñºÔ∏è  T√¨m th·∫•y h√¨nh ·∫£nh t·ª´ main selector: ${image}`);
            }
        }
    }

    // N·∫øu v·∫´n kh√¥ng c√≥, th·ª≠ t√¨m b·∫•t k·ª≥ h√¨nh ·∫£nh n√†o trong element
    if (!image) {
        const anyImg = $element.find('img').first();
        if (anyImg.length > 0) {
            image = anyImg.attr('src') ||
                anyImg.attr('data-src') ||
                anyImg.attr('data-lazy-src') ||
                anyImg.attr('data-original') ||
                anyImg.attr('data-url') ||
                anyImg.attr('data-thumb');

            if (image && image.length > 10 &&
                !image.includes('placeholder') &&
                !image.includes('loading') &&
                !image.includes('avatar') &&
                !image.includes('icon') &&
                !image.includes('logo') &&
                !image.startsWith('data:image/svg')) {
                console.log(`üñºÔ∏è  T√¨m th·∫•y h√¨nh ·∫£nh fallback: ${image}`);
            }
        }
    }

    return image;
}

// Function ƒë·ªÉ ki·ªÉm tra h√¨nh ·∫£nh c√≥ h·ª£p l·ªá kh√¥ng
async function validateImage(imageUrl) {
    try {
        if (!imageUrl) return false;

        // Chu·∫©n h√≥a URL
        if (!imageUrl.startsWith('http')) {
            return false;
        }

        const response = await axios.head(imageUrl, {
            timeout: 8000,
            headers: {
                'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36'
            }
        });

        if (response.status === 200) {
            const contentLength = response.headers['content-length'];
            const contentType = response.headers['content-type'];

            // Ki·ªÉm tra content type
            if (contentType && contentType.startsWith('image/')) {
                // Ki·ªÉm tra size (√≠t nh·∫•t 2KB ƒë·ªÉ tr√°nh ·∫£nh nh·ªè)
                if (!contentLength || parseInt(contentLength) > 2000) {
                    console.log(`‚úÖ H√¨nh ·∫£nh h·ª£p l·ªá: ${imageUrl} (${contentLength ? Math.round(contentLength / 1024) + 'KB' : 'Unknown size'})`);
                    return true;
                } else {
                    console.log(`‚ö†Ô∏è  H√¨nh ·∫£nh qu√° nh·ªè (${contentLength} bytes): ${imageUrl}`);
                    return false;
                }
            } else {
                console.log(`‚ö†Ô∏è  Kh√¥ng ph·∫£i file h√¨nh ·∫£nh: ${contentType}`);
                return false;
            }
        }
        return false;
    } catch (error) {
        console.log(`‚ùå H√¨nh ·∫£nh kh√¥ng kh·∫£ d·ª•ng: ${imageUrl} - ${error.message}`);
        return false;
    }
}

// Test scraping h√¨nh ·∫£nh t·ª´ VnExpress
async function testVnExpressImages() {
    try {
        console.log('üß™ Test l·∫•y h√¨nh ·∫£nh t·ª´ VnExpress...');

        const response = await axios.get('https://vnexpress.net/suc-khoe', {
            headers: {
                'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36'
            },
            timeout: 10000
        });

        const $ = cheerio.load(response.data);

        const source = {
            name: 'VnExpress S·ª©c kh·ªèe',
            selector: {
                articles: '.item-news',
                title: '.title-news a',
                link: '.title-news a',
                summary: '.description',
                image: '.thumb-art img',
                category: 'S·ª©c kh·ªèe'
            },
            imageSelectors: [
                '.thumb-art img',
                '.item-news img',
                '.news-item img',
                '.pic img'
            ]
        };

        let count = 0;
        const results = [];

        $('.item-news').each(async (index, element) => {
            if (count >= 3) return; // Test 3 b√†i ƒë·∫ßu

            const $element = $(element);
            const title = $element.find('.title-news a').text().trim();
            const image = extractImageFromElement($element, source);

            if (title && image) {
                // Chu·∫©n h√≥a URL
                let fullImageUrl = image;
                if (!image.startsWith('http')) {
                    const baseUrl = 'https://vnexpress.net';
                    fullImageUrl = image.startsWith('/') ? `${baseUrl}${image}` : `${baseUrl}/${image}`;
                }

                console.log(`\nüì∞ B√†i ${count + 1}: ${title}`);
                console.log(`üñºÔ∏è  H√¨nh g·ªëc: ${fullImageUrl}`);

                const isValid = await validateImage(fullImageUrl);
                results.push({
                    title,
                    image: fullImageUrl,
                    valid: isValid
                });

                count++;
            }
        });

        // Ch·ªù m·ªôt ch√∫t ƒë·ªÉ c√°c async calls ho√†n th√†nh
        setTimeout(() => {
            console.log('\nüìä K·∫øt qu·∫£ t·ªïng h·ª£p:');
            console.log(`‚úÖ T·ªïng s·ªë b√†i test: ${results.length}`);
            console.log(`‚úÖ H√¨nh ·∫£nh h·ª£p l·ªá: ${results.filter(r => r.valid).length}`);
            console.log(`‚ùå H√¨nh ·∫£nh kh√¥ng h·ª£p l·ªá: ${results.filter(r => !r.valid).length}`);
        }, 5000);

    } catch (error) {
        console.error('‚ùå L·ªói test VnExpress:', error.message);
    }
}

